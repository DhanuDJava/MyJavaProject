package MyJavaProgramming;

public class DataTypes {
	
	public static void main(String [] args){
		
		String name = "Dhananjay";					//String is Non-primitive Data Type
		String line = "This is a java Program.";
		
		System.out.println(name);
		System.out.println(line);
		
		int num= 10;								//Primitive Data Type
		System.out.println(num);
		
		long num1= 820850806;						//Primitive Data Type
		System.out.println(num1);
		
		boolean bool1= true;						//Primitive Data Type
		boolean bool2= false;
		System.out.println(bool1);
		System.out.println(bool2);
	
		char ch= 'a';								//Primitive Data Type
		System.out.println(ch);
		
		float f1= -1.23456789f;								//Primitive Data Type
		System.out.println(f1);
		
		double d1= -145.65556412789633154545698;
		System.out.println(d1);
		
		
	}

}
