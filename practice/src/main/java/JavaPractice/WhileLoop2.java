package JavaPractice;

public class WhileLoop2 {
	
	public static void main(String[] args) {
		
		int num=7;
		
		//start
		int i=10;
		
		//condtion
		while(i>=1) {
			//System.out.println(num*i);
			System.out.println(num +" * "+i +" = "+ (num*i));
			
		//increase or decrease
			i=i-1;
		}
	}

}
