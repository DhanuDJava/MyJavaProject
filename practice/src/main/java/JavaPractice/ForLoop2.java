package JavaPractice;

public class ForLoop2 {
	
	public static void main(String [] args) {
		
		int num= 4;
		
		//start
		//condition
		//increase or decrease
		
		for(int i=1; i<=10; i=i+1) {
			//System.out.println(num*i);
			System.out.println(num +" * "+i +" = "+ (num*i));
		}
		
		//4 * 1 = 4
	}
		 
}
